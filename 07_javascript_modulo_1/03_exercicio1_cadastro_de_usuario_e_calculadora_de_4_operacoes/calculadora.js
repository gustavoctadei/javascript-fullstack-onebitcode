let x = Number( prompt("Insira o primeiro número:") )
let y = Number( prompt("Insira o segundo número:") )

soma = x + y
subtracao = x - y
multiplicacao = x * y
divisao = x / y

let resultado = "Soma: " + soma + "\n"
resultado += "Subtração: " + subtracao + "\n"
resultado += "Multiplicação: " + multiplicacao + "\n"
resultado += "Divisão: " + divisao + "\n"

alert(resultado)